// Tipos de datos primitivos en JavaScript
console.log("Tipos de datos primitivos en JavaScript:");
let nombre = "Juan"; // String
let edad = 30; // Number
let esEstudiante = true; // Boolean
let direccion; // Undefined
let telefono = null; // Null
let simbolo = Symbol("id"); // Symbol
let bigIntNumero = 9007199254740991n; // BigInt

console.log("Nombre:", nombre);
console.log("Edad:", edad);
console.log("Es estudiante:", esEstudiante);
console.log("Dirección:", direccion);
console.log("Teléfono:", telefono);
console.log("Símbolo:", simbolo);
console.log("BigInt Número:", bigIntNumero);

// Clear
console.log();

// Uso de typeof para verificar tipos de datos
console.log("Verificación de tipos de datos usando typeof:");
console.log("Tipo de nombre:", typeof nombre);
console.log("Tipo de edad:", typeof edad);
console.log("Tipo de esEstudiante:", typeof esEstudiante);
console.log("Tipo de direccion:", typeof direccion);
console.log("Tipo de telefono:", typeof telefono);
console.log("Tipo de simbolo:", typeof simbolo);
console.log("Tipo de bigIntNumero:", typeof bigIntNumero);